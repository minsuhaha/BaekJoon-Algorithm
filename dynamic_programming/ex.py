import sys
n = int(sys.stdin.readline())

# #조건 0으로 시작 x, 연속적인 1 불가
# # n=1 -> 1 /1개
# # n=2 -> 10 / 1개
# # n=3 -> 100, 101 -> 2개
# # n=4 -> 1000,1001,1010 -> 3개
# # n=5 -> 10000,10001,10010,10100,10101 -> 5개
# # n=6 -> 100000,100001,100010,100100,101000,101001,100101,101010 -> 8개
# # n=7 -> 1000000,1000001,1000010,1000100,1001000,1010000,1000101,1001001,1010001,1010100,1010101,1001010,1000101 ->13개

# # 1 1 2 3 4 8 13
# # dp 초기화 1부터 인덱스를 매길거임 그래서 n+1

# dp = [0]*(n+1)

dp = [0, 1, 1] 

for i in range(3, n+1):
    result = dp[i-2] + dp[i-1]
    dp.append(result)

print(dp[n])
